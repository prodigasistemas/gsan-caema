package gcom.gui.cadastro;

import gcom.relatorio.RelatorioBean;

/**
 * [UC1561] - Liberar os Logradouros para Atualização no GSAN
 * 
 * @author Anderson Cabral
 * @since 25/09/2013
 */
public class ImovelEnderecoArquivoTextoBean implements RelatorioBean, Comparable<ImovelEnderecoArquivoTextoBean> {
	
	private String idLogradouro;
	private String nomeLogradouro;
	private String matriculaImovel;
	private String inscricaoImovel;
	private String numeroImovel;
	private String complementoEndereco;
	private String nomeBairro;
	private String nomeMunicipio;
	private String codigoCep;
	private String data;
	
	public String getIdLogradouro() {
		return idLogradouro;
	}
	public void setIdLogradouro(String idLogradouro) {
		this.idLogradouro = idLogradouro;
	}
	public String getNomeLogradouro() {
		return nomeLogradouro;
	}
	public void setNomeLogradouro(String nomeLogradouro) {
		this.nomeLogradouro = nomeLogradouro;
	}
	public String getMatriculaImovel() {
		return matriculaImovel;
	}
	public void setMatriculaImovel(String matriculaImovel) {
		this.matriculaImovel = matriculaImovel;
	}
	public String getInscricaoImovel() {
		return inscricaoImovel;
	}
	public void setInscricaoImovel(String inscricaoImovel) {
		this.inscricaoImovel = inscricaoImovel;
	}
	public String getNumeroImovel() {
		return numeroImovel;
	}
	public void setNumeroImovel(String numeroImovel) {
		this.numeroImovel = numeroImovel;
	}
	public String getComplementoEndereco() {
		return complementoEndereco;
	}
	public void setComplementoEndereco(String complementoEndereco) {
		this.complementoEndereco = complementoEndereco;
	}
	public String getNomeBairro() {
		return nomeBairro;
	}
	public void setNomeBairro(String nomeBairro) {
		this.nomeBairro = nomeBairro;
	}
	public String getNomeMunicipio() {
		return nomeMunicipio;
	}
	public void setNomeMunicipio(String nomeMunicipio) {
		this.nomeMunicipio = nomeMunicipio;
	}
	public String getCodigoCep() {
		return codigoCep;
	}
	public void setCodigoCep(String codigoCep) {
		this.codigoCep = codigoCep;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	public int compareTo(ImovelEnderecoArquivoTextoBean o) {
		return o.getIdLogradouro().compareTo(this.getIdLogradouro());
	}
	
	
}
