package gcom.cobranca.cobrancaporresultado;



public class DadosPropocionalidadeHelper {
	private String desc;
	private String qtdMinContas;
	private String percentualFaixa;
	private String percentualImoveis;
	
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getQtdMinContas() {
		return qtdMinContas;
	}
	public void setQtdMinContas(String qtdMinContas) {
		this.qtdMinContas = qtdMinContas;
	}
	public String getPercentualFaixa() {
		return percentualFaixa;
	}
	public void setPercentualFaixa(String percentualFaixa) {
		this.percentualFaixa = percentualFaixa;
	}
	public String getPercentualImoveis() {
		return percentualImoveis;
	}
	public void setPercentualImoveis(String percentualImoveis) {
		this.percentualImoveis = percentualImoveis;
	}
}
